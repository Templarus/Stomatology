    private void butOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butOkActionPerformed
        ArrayList<JCheckBoxSprRab> checkList = new ArrayList<>();
        
        for(int i = 0; i < panRabZub.getComponentCount(); i++){
            JCheckBoxSprRab check = (JCheckBoxSprRab)panRabZub.getComponent(i);
            if(check.isSelected()){
                checkList.add(check);
            }
        }
        for(int i = 0; i < panRabPoverh.getComponentCount(); i++){
            JCheckBoxSprRab check = (JCheckBoxSprRab)panRabPoverh.getComponent(i);
            if(check.isSelected()){
                checkList.add(check);
            }
        }
                for(int i = 0; i < panRabKoren.getComponentCount(); i++){
            JCheckBoxSprRab check = (JCheckBoxSprRab)panRabKoren.getComponent(i);
            if(check.isSelected()){
                checkList.add(check);
            }
        }
         for(int i = 0; i < panRabDesna.getComponentCount(); i++){
            JCheckBoxSprRab check = (JCheckBoxSprRab)panRabDesna.getComponent(i);
            if(check.isSelected()){
                checkList.add(check);
            }
        }
        if(Start.sDB.setIsklPepvichRab(checkList, idPervich) != 0) {
             JOptionPane.showMessageDialog(null, "Данные успешно обновлены", "Справочник исключений", JOptionPane.INFORMATION_MESSAGE);
             dispose();
        }
        
    }//GEN-LAST:event_butOkActionPerformed

