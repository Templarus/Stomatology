    public SprPervichIskRabEdit(int idPervich, String namePervich,String nameKateg) {
        initComponents();
        this.idPervich = idPervich;
        namePervichObs.setText(namePervich + " / " + nameKateg);
        //Заполняем панельки
        int y = 10;
        int y1 = 10;
        int y2 = 10;
        int y3 = 10;
        for(JCheckBoxSprRab check : Start.sDB.getPervichRab(idPervich)){
            if(check.getKategRab() == 3){
                panRabZub.add(check);
                check.setBounds(10, y, 150, 20);
                y = y + 20;
            }
            if(check.getKategRab() == 1){
                panRabPoverh.add(check);
                check.setBounds(10, y1, 150, 20);
                y1 = y1 + 20;
            }
            if(check.getKategRab() == 2){
                panRabKoren.add(check);
                check.setBounds(10, y2, 150, 20);
                y2 = y2 + 20;
            }
            if(check.getKategRab() == 5){
                panRabDesna.add(check);
                check.setBounds(10, y3, 150, 20);
                y3 = y3 + 20;
            }
                check.setSelected(check.isSelect());
                check.setVisible(true);
        }
    }

